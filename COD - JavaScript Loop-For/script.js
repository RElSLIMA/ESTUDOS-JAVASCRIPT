// USANDO VAR
for (var i = 54; i > -60; i--) {
  console.log(i);
}
console.log("i =", i);

for (var a = 1; a < 60; a++) {
  console.log(a);
}
console.log("a =", a);

// USANDO LET
for (let e = 4; e > -6; e--) {
  console.log(e);
  console.log("e =", e);
}

for (let o = 1; o < 6; o++) {
  console.log(o);
  console.log("o =", o);
}
